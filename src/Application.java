
public class Application {

	public static void main(String[] args) {
		int myNumber = 88;
		short myShort = 400; 
		long myLong = 5000;
		
		double myDouble = 6.5643;
		float myFloat = 4.5f;
		
		char myChar = 'u'; // hold one character only
		boolean myBool = true; // only true or false
		
		byte myByte = 127; // max it can hold
		
		System.out.println(myNumber + "\n" + myShort + "\n" + myLong + "\n" + myDouble + "\n" + myFloat + "\n" + myChar + "\n" + myBool + "\n" + myByte);		
	}
}
