/**
 * If Statements with some for loops 
 */
public class IfStatement {

	public static void main(String[] args) {
		String x = "I am true";
		String y = "I am false";
		
		for(int i=0; i< 5; i++)
		{
			if(i == 4)
			{
				System.out.println(x);
			}
			else
			{
				System.out.println(y);
			}
		}

	}

}
